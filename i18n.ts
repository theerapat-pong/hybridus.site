export type Language = keyof typeof translations;
type TranslationKey = keyof (typeof translations)['my'];

export const useTranslations = (lang: Language) => {
  return function t(key: TranslationKey, replacements?: { [key: string]: string }): string {
    let translation = translations[lang][key] || translations.my[key];
    if (replacements) {
        Object.keys(replacements).forEach(rKey => {
            translation = translation.replace(`{${rKey}}`, replacements[rKey]);
        });
    }
    return translation;
  };
};